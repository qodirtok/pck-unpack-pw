-- Get Enchant Stone
-- input 1 equipment type, 2 equipment level
EquipEnchantFunc = {}

function EquipEnchantFunc:GetEnchantStone(equiptype, equiplevel)
	if EquipEnchant[equiptype] == nil or EquipEnchant[equiptype][equiplevel] == nil then
		return nil
	end
	
	local EStoneList = EquipEnchant[equiptype][equiplevel]
	local ilist = {}
	local l = 0
	for k, v in ipairs(EStoneList) do
		l = l + 1
		ilist[l] = v
	end
	
	return ilist
end