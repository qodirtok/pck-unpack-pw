-- 活动配置格式：
-- EventWorldID[world_tag] = 
-- {
-- 	EventID = 
-- 	{
--		[event_id] = 
--		{
-- 			total_time = time_value,
-- 			ctrl_param = param_id,
-- 			param_list = {param_id1, param_id2, ...},
--		},
-- 	},
-- }
-- world_tag : 地图的世界ID 例：河北1 西凉2 关中7
-- event_id : 活动ID 同一个地图中的活动ID不能重复
-- total_time : 活动持续时间 单位秒，如果填0则不在客户端显示
-- ctrl_param : 活动控制场景参数ID 此场景参数为1代表活动开始；为0代表活动未开始
-- param_list : 此活动需要在客户端显示的场景参数ID



