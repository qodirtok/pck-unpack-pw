--equiptype = 1  表示防具

function help_tip:ShowSpecailDesc(equiptype, class, pname, eqname)
local tstring = ""
	if equiptype == 0 and SpecailDesc0[class] ~= nil then
		tstring = SpecailDesc0[class]
	elseif equiptype ~= 0 and SpecailDesc1[class] ~= nil then
		tstring = SpecailDesc1[class]
	end
	return tstring
end

