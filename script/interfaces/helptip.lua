--[[ 总共128个事件，当第一次发生的时候，客户端调用一下函数进行处理；

1，	玩家第一次获得护卫时（使用投诚令后）
2，	第一个护卫升到1级时
3，	玩家第一次打开Pet.xml界面时

4，	玩家第一次召出护卫

5，	第一个护卫第一次可升级时

6，	第一个护卫到达100级时
7，	第一次获得扩护卫技能格道具
8，	第一次获得护卫回血道具
9， 第一次打开PetProperty.xml界面

]]--

function help_tip:FirstDoSth( iEvent )
local ret = GameApi.GetPlayerBasicProp(0)
	if ret.level < 40 then
		return
	end
	if iEvent == 1 then
		DlgApi.ShowItem("Win_CharHead","Gfx_PetHeadMini",true)
		DlgApi.ShowItem("Win_CharHead","Gfx_Pointer",true)
	elseif iEvent == 2 then
		--DlgApi.ShowItem("Win_PetMan", "Gfx_Plus" , true) --+点按钮闪烁

	elseif iEvent == 3 then
		DlgApi.ShowItem("Win_PetMan","Gfx_Summon",true) --召按钮发光

	elseif iEvent == 4 then
		DlgApi.ShowDialog("Win_PetHelp14", true)
	elseif iEvent == 5 then
		DlgApi.ShowDialog("Win_PetMan", true)
		DlgApi.ShowItem("Win_PetMan", "Gfx_Levelup", true) --升级按钮发光
	elseif iEvent == 6 then
		DlgApi.ShowDialog("Win_PetHelp15", true)
	elseif iEvent == 7 then
		DlgApi.ShowDialog("Win_PetHelp5", true)
	elseif iEvent == 8 then
		DlgApi.ShowDialog("Win_PetHelp6", true)
		DlgApi.ShowDialog("Win_PetHelp7", true)
	elseif iEvent == 9 then
		DlgApi.ShowDialog("Win_PetHelp10", true)
	end
end
